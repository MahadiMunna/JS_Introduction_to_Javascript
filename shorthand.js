// variable declaration 
var age = 35;

// value update 
age = 36;

console.log(age);

// add something with exixting variable

age = age + 1; 
console.log(age);

// shorthand
age += 1;
console.log(age);

age++; // adding 1 with existing value 
console.log(age);
age--; // substracting 1 with existing value 
console.log(age);